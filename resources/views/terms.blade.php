                    제 18 조 (본 서비스의 중지 · 중단) <br>
                    회사는 다음 각 호의 어느 하나에 해당하는 경우 본 서비스의 운영을 중지 또는 중단 할 수 있습니다. <br>
				    <ol class = "decimal0">
				        <li> 서비스 시스템의 보수 또는 공사를 정기적으로 또는 긴급하게 실시하는 경우, 또는 우리의 시스템 장애 등 부득이한 경우. </li>
				                                
				        <li> 전쟁, 폭동, 노동 쟁의, 지진, 분화, 홍수, 해일, 화재, 정전 기타 사유의 여하를 불문하고 불가항력으로 인하여 본 서비스의 제공이 정상적으로 할 수 없게 된 경우. </li>
				                                
				        <li> 국가 기관 또는 지방 자치 단체의 규정, 명령에 의한 경우 또는 다른 전기 통신 사업자 등이 본 서비스의 제공을 중지 또는 중단 된 경우. </li>
				                                
				        <li> 기타 당사가 본 서비스의 운영상 일시적인 중단이 필요하다고 판단한 경우. </li>
				    </ol>
				    <p> 2. 당사는 전항의 규정에 따라 본 서비스의 운영을 중지 또는 중단 할 경우에는 이용자에 대하여 그 취지를 사전에 통지하여야합니다. 다만, 긴급 어쩔 수없는 경우는 그러하지 않습니다. <br>
                        <br>
                        3. 당사는 본 조항에 근거한 본 서비스의 중지 또는 중단으로 인해 이용자 또는 제 3자가 입은 손해에 대해 책임을지지 않습니다합니다. <br>
                        <br>
                        제 19 조 (해제) <br>
                        당사는 이용자가 본 약관 계약 약관 및 기타 약관 등을 위반 한 경우 또는 이용자의 본 서비스 이용이 부적당하다고 당사가 판단하는 경우, 미리 사전 통지없이 서비스 이용 계약 해제 할 수있는 것으로합니다. <br>
                        <br>
                        2. 당사는 전항에 따른 서비스 이용 계약의 해제에 따라 이용자 또는 제 3자가 입은 손해에 대해 책임을지지 않습니다합니다.
				    </p>
				    <h2> 제 7 장 손해 배상 등 </h2>
				    <p> 제 20 조 (손해 배상) <br>
                        손해에 대해서는 당사가 정하는 계약 약관에 따르는 것으로합니다. <br>
                        <br>
                        제 21 조 (면책 조항) <br>
                        당사는 본 서비스의 제공과 관련하여 이용자에게 발생한 손해에 대해 책임을지지 않습니다. <br>
                        <br>
                        2. 당사는 이용자가 사용하는 통신 기기 및 소프트웨어 등에 대한 어떠한 동작 보증은하지 않습니다. <br>
                        <br>
                        제 22 조 (준거법) <br>
                        본 규약의 성립, 효력, 이행 및 해석에 관해서는 일본 법이 적용되는 것으로합니다. <br>
                        <br>
                        제 23 조 (협의) <br>
                        본 서비스와 관련하여 이용자와 당사 사이에 분쟁이 발생한 경우에는 당해 당사자가 모두 성실하게 협의하여야합니다. <br>
                        <br>
                        제 24 조 (관할 법원) <br>
                        본 서비스에 걸리는 분쟁을 해결함에 있어서는 도쿄 지방 재판소 또는 도쿄 간이 재판소를 제 1 심의 전속 적 합의 관할 법원으로합니다. <br>
