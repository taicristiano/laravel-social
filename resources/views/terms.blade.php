                    제 17 조 (신청자 및 이용자 정보의 활용) <br>
                    당사는 「고객 정보의 활용에있어서의 개인 정보 보호 관행 "에 근거하여 개인을 특정하지 않는 안전한 형태로 가공 한 후, 적정하게 취급합니다. <br>
                    "고객 정보 활용에있어서의 개인 정보 보호 관행"은 <a href="/jp/privacy/data/privacy_utilization.pdf" target="_blank"> 여기 </a>를 확인하시기 바랍니다. 또한 고객 정보의 활용의 설정은 <a href="https://service2.wi2.ne.jp/wi2was/usingSettingInput" target="_blank"> 여기 </a>로 해드립니다. <br>
