                        본 서비스의 이용을 희망하는 신청자 (이하 「신청자」라고 다음 조의 서비스 이용 계약의 성립을 가지고 「이용자」라고합니다)는 본 규약 및 계약 약관에 동의 보신 당사 소정의 절차에 따라 본 서비스를 신청해야합니다. 또한, 신청자가 미성년자 인 경우에는 친권자의 동의를 얻어 신청 해주십시오. <br>
                        <br>
                        제 4 조 (서비스 이용 계약의 성립) <br>
                        신청자가 전조에 따라 본 서비스 이용 신청을하고, 등록이 완료된 시점에서 본 서비스 이용 계약이 성립합니다. <br>
                        <br>
                        제 5 조 (서비스 이용 조건) <br>
                        이용자는 자기 책임과 부담으로 본 서비스를 이용하기 위해 필요한 통신 기기, 소프트웨어 등을 준비해야합니다. <br>
                        <br>
                        제 6 조 (서비스 이용 자격의 양도 제한) <br>
                        이용자는 본 서비스를 이용할 권리를 타인에게 양도 할 수없는 것으로합니다. <br>
                        <br>
                        제 7 조 (이용자의 본 서비스 이용의 취소) <br>
                        이용자는 본 서비스의 이용을 取りやめよ하고자하는 때에는 그 것을 미리 당사가 지정하는 방법으로 당사에 통지하여야합니다.
				    </p>
				    <h2> 제 3 장 서비스 </h2>
				    <p> 제 8 조 (제공 서비스) <br>
                        당사는 이용자에 대해 이용 약관, 계약 약관에 따라 이용 약관, 계약 약관에 규정하는 한도에서 서비스를 제공합니다. 또한 본 서비스의 이용에있어서 당사 또는 제 3자가 별도로 제공하는 개별 약관 또는 기타 약관 (이하 "기타 약관 등"이라고합니다.)이있는 경우, 이용자는 약관 이외에 기타 약관 등에 동의하고 그들에 따라야합니다. <br>
                        <br>
                        2. 당사는 본 서비스에 대한 본 서비스의 내용의 전부 또는 일부를 변경 할 수 있습니다. <br>
                        <br>
                        제 9 조 (제 3자가 제공하는 정보의 이용) <br>
                        이용자는 제 3자가 제공하는 정보의 이용에있어서 책임은 각 정보 제공자에게 귀속하고 있는지 및 당사가 해당 정보 제공의 당사자가 아닌 것에 동의합니다. <br>
                        <br>
                        제 10 조 (제 3자가 제공하는 정보의 내용을 보증) <br>
                        당사는 본 서비스를 통해 제 3자가 제공하는 제품, 서비스 및 정보 그 완전성, 정확성, 확실성, 유용성 등에 대해 어떠한 보증도하지 않습니다. <br>
                        <br>
                        2. 당사는 이용자가 제삼자 제공하는 상품, 서비스 또는 정보를 이용한 것에 대해 이용자와 제삼자와의 사이에 분쟁이 발생한 경우 책임을지지 않습니다.
				    </p>
				    <h2> 제 4 장 이용자의 의무 등 </h2>
				    <p> 제 11 조 (금지 사항) <br>
                        이용자는 본 서비스의 이용에있어서 다음 각호의 행위를 하여서는 아니하되, 다음 각 호의 행위를하고있는 당사가 판단하는 경우, 당사는 본 서비스의 이용을 중지 할 수 있습니다.
				    </p>
				    <ol>
				        <li> 타사 또는 당사의 저작권 또는 기타 권리를 침해하는 행위 또는이를 침해 할 우려가있는 행위. </li>
				                                
				        <li> 타사 또는 당사의 재산 혹은 프라이버시를 침해하는 행위 또는이를 침해 할 우려가있는 행위. </li>
				                                
				        <li> 전호의 외, 제삼자 또는 당사에 불이익 혹은 손해를주는 행위 또는 줄 우려가있는 행위. </li>
				                                
				        <li> 타사 또는 당사를 비방하는 행위. </li>
				                                
				        <li> 공서 양속에 반하는 행위 (음란, 성매매, 폭력, 잔혹, 학대 등), 또는 그 우려가있는 행위 또는 공서 양속에 반하는 정보를 제삼자에게 제공하는 행위. </li>
				                                
				        <li> 범죄 행위 또는 범죄 적 행위에 결부 행위, 또는 그 우려가있는 행위. </li>
				                                
				        <li> 선거 기간 중인지 아닌지를 불문하고, 선거 운동 또는 이와 유사한 행위. </li>
				                                
				        <li> 본 서비스를 재판매, 임대하는 등 본 서비스 자체를 영리를 목적으로하는 행위. </li>
				                                
				        <li> 다단계 사기 (피라미드)를 개설하거나이를 권유하는 행위. </li>
				                                
				        <li> 불특정 다수에게 배포하는 광고 · 선전 · 권유 등 또는 믿을 수없는 정보 또는 혐오감을 품게 또는 그 우려가있는 이메일 (괴롭힘 이메일)을 전송하는 행위. </li>
				                                
				        <li> 타사 또는 당사에 메일 수신을 방해하는 행위, 또는 연쇄적인 메일 전송을 의뢰 또는 해당 요청에 따라 전송하는 행위. </li>
				                                
				        <li> 제삼자로 위장하여 본 서비스를 이용하는 행위. </li>
				                                
				        <li> 본 서비스를 통해 당사 또는 제삼자에 대한 무단 액세스 또는 변경, 삭제 등의 불법 행위. </li>
				                                
				        <li> 컴퓨터 바이러스 등의 유해 프로그램을 본 서비스를 통해 또는 서비스와 관련하여 사용하거나 제공하는 행위. </li>
				                                
				        <li> 타사 또는 당사에게 폐 · 불이익을주는 행위 본 서비스에 차질을 빚을 우려가있는 행위 본 서비스의 운영을 방해하는 행위. </li>
				                                
				        <li> 본 서비스를 이용하여 본 서비스를 직접 또는 간접적으로 이용하는 자에 해당 이용자에 대해 심각한 지장을주는 행위, 또는 그 우려가있는 행위. </li>
				                                
				        <li> 기타 법령에 위반하거나 위반할 우려가있는 행위. </li>
				                                
				        <li> 기타 당사가 부적절하다고 판단하는 행위. </li>
				    </ol>
				    <p> 제 12 조 (이용자의 책임) <br>
                        전조 (금지 사항)에 해당하는 이용자의 행위에 의해 당사 및 제삼자에게 손해가 발생한 경우, 이용자의 자격을 상실한 후에도 이용자는 손해 배상 등 모든 법적 책임을 져야합니다. <br>
                        <br>
                        제 13 조 (이용자의 자기 책임) <br>
                        이용자는 본 서비스를 이용하여 업로드 또는 다운로드 한 정보 나 파일에 관련하여 어떠한 손해를 입은 경우 또는 어떠한 법적 책임 경우에는 모든 책임은이를 처리하고 당사에 대해 아무런 청구도 이루어 않고 폐를 끼치 지 않는 것으로합니다. <br>
                        <br>
                        제 14 조 (소유권 및 지적 재산권) <br>
                        본 서비스를 구성하는 모든 프로그램, 소프트웨어, 서비스, 절차, 상표, 상호 또는 제 3자가 제공하는 서비스 또는 그와 관련된 기술 전반에 대한 소유권 및 지적 재산권은 당사 또는 그 제공자에게 귀속하는 물건합니다. <br>
                        <br>
                        제 15 조 (저작권) <br>
                        이용자는 권리자의 허락 없이는 어떠한 방법으로도 본 서비스를 통해 제공되는 모든 정보 또는 파일에 대해 저작권법에서 정하는 이용자 개인의 사적 이용을위한 복제의 범위를 넘어て 이용을 할 수없는 것으로합니다. <br>
                        <br>
                        2. 이용자는 권리자의 허락 없이는 어떠한 방법으로도 본 서비스를 통해 제공되는 모든 정보 또는 파일에 대해 제 3 자로 하여금 사용하거나 공개하거나 할 수없는 것 합니다. <br>
                        <br>
                        3. 전 2 항의 규정에 위반하여 분쟁이 발생한 경우, 이용자는 자신의 비용과 책임하에 분쟁을 해결하고, 우리를 어떠한 경우에도 면책 당사에 대하여 손해를 끼친 없는 것으로합니다.
				    </p>
				    <h2> 제 5 장 개인 정보 보호 </h2>
				    <p> 제 16 조 (개인 정보의 이용) <br>
